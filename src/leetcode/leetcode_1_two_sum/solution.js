// https://leetcode.com/problems/two-sum/discuss/1183990/Simple-Javascript-Solution-or-O(N)-Time-Complexity-or-Map
var twoSum = function (nums, target) {
  let map = new Map();
  for (let i = 0; i < nums.length; i++) {
    if (map.has(target - nums[i])) {
      return [map.get(target - nums[i]), i];
    }
    map.set(nums[i], i);
  }
};

const input = [[2, 7, 11, 15], 9];
console.log(twoSum(...input));
